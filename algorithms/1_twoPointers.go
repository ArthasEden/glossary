package algorithms

/*
Два указателя - 17,7%

Более одного массива / строки?
│
├── Да → каждому по указателю
│
└── Нет
    │
    ├── Нужна in-place модификация?
        │
        ├── Да → быстрый и медленный указатель
        │
        └── Нет → указатели с двух сторон
*/

/*
С двух сторон - 29,2%

Время: O(n)
Память: O(1)

Пример: Найти два числа в отсортированном массиве, сумма которых равна target
Вход: nums = [1,2,4,6,10], target = 8
Выход: [1,3]

Флаги:
1. По условию дан один осортированный массив
2. Задача на проверку палиндрома (возможны усложнения)
3. Ответ формируется ща счет сужения области с двух сторон
*/
func twoSum(nums []int, target int) []int {
	if len(nums) < 2 {
		return []int{-1, -1}
	}

	l := 0
	r := len(nums) - 1

	for l < r {
		currSum := nums[l] + nums[r]

		if currSum == target {
			return []int{l, r}
		} else if currSum > target {
			r--
		} else {
			l++
		}
	}
	return []int{-1, -1}
}

/*
Каждому по указателю - 41,6%

Время: O(n+m), где n, m размеры входных массивов
Память: O(min(n,m))

Пример: Найти пересечение двух отсортированных массивов
Вход: nums1 = [1,2,2,3], nums2 = [2,2,4]
Выход: [2,2]

Флаги:
1. Даны несколько массивов или строк
2. Нужно искать объединение/перечение и т.д. у двух последовательностей
*/

func intersect(nums1 []int, nums2 []int) []int {
	p1 := 0
	p2 := 0
	result := make([]int, 0)

	for p1 < len(nums1) && p2 < len(nums2) {
		if nums1[p1] < nums2[p2] {
			p1++
		} else if nums1[p1] > nums2[p2] {
			p2++
		} else {
			result = append(result, nums1[p1])
			p1++
			p2++
		}
	}
	return result
}

/*
Быстрый и медленный - 29,2%

Время: O(n)
Память: O(1)

Пример: Переместить все нули в конец массива, сохранив порядок остальных элементов
Вход: nums = [0,1,0,3,12]
Выход: [1,3,12,0,0]

Флаги:
1. Нужна in-place модификация для строки или массива. In-place - это значит изменять данные прямо в той же памяти, без создания новой структуры.
2. Требуется сохранить исходный порядок
*/

func moveZeros(nums []int) []int {
	slow := 0
	fast := 0

	for fast < len(nums) {
		if nums[fast] != 0 {
			nums[slow], nums[fast] = nums[fast], nums[slow]
			slow++
		}
		fast++
	}
	return nums
}
